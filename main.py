
import asyncio, os
from videosdk.agents import Agent, AgentSession, CascadingPipeline, JobContext, RoomOptions, WorkerJob,ConversationFlow
from videosdk.plugins.silero import SileroVAD
from videosdk.plugins.turn_detector import TurnDetector, pre_download_model
from videosdk.plugins.deepgram import DeepgramSTT
from videosdk.plugins.openai import OpenAILLM
from videosdk.plugins.elevenlabs import ElevenLabsTTS
from typing import AsyncIterator

# Pre-downloading the Turn Detector model
pre_download_model()

class MyVoiceAgent(Agent):
    def __init__(self):
        super().__init__(instructions="""You are an engaging and knowledgeable AI Voice Agent Teacher dedicated to supporting learners of all ages and backgrounds. Your primary role is to explain concepts, answer questions, provide clear step-by-step guidance, and facilitate interactive learning across a wide range of academic subjects and skills. Always maintain a patient, encouraging, and approachable tone. Adapt your explanations to the learnerâ€™s level of understanding, offering examples, analogies, and positive feedback to foster confidence and curiosity. Encourage active participation by asking questions, prompting reflection, and suggesting practice exercises. If a learner struggles, break down complex topics into simpler parts and offer guidance without judgment. Respect diverse learning styles and create a safe, inclusive, and motivating environment. If a question falls outside your expertise or contains inappropriate content, politely guide the learner back to educational topics. Your interaction style should be supportive, attentive, and inspiring, ensuring every learner feels heard and empowered to succeed.""")
    async def on_enter(self): await self.session.say("Hello! How can I help you today regarding AI Voice Agent Teacher?")
    async def on_exit(self): await self.session.say("Goodbye!")

async def start_session(context: JobContext):
    # Create agent and conversation flow
    agent = MyVoiceAgent()
    conversation_flow = ConversationFlow(agent)

    # Create pipeline
    pipeline = CascadingPipeline(
        stt=DeepgramSTT(model="nova-2", language="en"),
        llm=OpenAILLM(model="gpt-4o"),
        tts=ElevenLabsTTS(model="eleven_flash_v2_5"),
        vad=SileroVAD(threshold=0.35),
        turn_detector=TurnDetector(threshold=0.8)
    )

    session = AgentSession(
        agent=agent,
        pipeline=pipeline,
        conversation_flow=conversation_flow
    )

    try:
        await context.connect()
        await session.start()
        # Keep the session running until manually terminated
        await asyncio.Event().wait()
    finally:
        # Clean up resources when done
        await session.close()
        await context.shutdown()

def make_context() -> JobContext:
    room_options = RoomOptions(
     #  room_id="YOUR_MEETING_ID",  # Set to join a pre-created room; omit to auto-create
        name="VideoSDK Cascaded Agent for AI Voice Agent Teacher",
        playground=True
    )

    return JobContext(room_options=room_options)

if __name__ == "__main__":
    job = WorkerJob(entrypoint=start_session, jobctx=make_context)
    job.start()
